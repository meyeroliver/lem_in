#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int	i;

	i = -1;
	if (fd != -1)
	{
		while (s[++i])
			ft_putstr_fd(s, fd);
		ft_putchar_fd('\n', fd);
	}
}
